#include "../../inc/libft.h"

int	is_space(int c)
{
	if (c == '\t' || \
			c == '\n' || \
			c == '\v' || \
			c == '\r' || \
			c == '\f' || \
			c == ' ')
		return (1);
	return (0);
}
